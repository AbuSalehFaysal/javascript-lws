// JS Set Date Methods

const d = new Date();
d.setFullYear(2020); // set the year to 2020
d.setFullYear(2020, 11, 10); // we can also set the month and day as well in setFullYear method.
d.setMonth(11); // Set month to december, because starts from 0.
d.setDate(); // set the date
d.setHours(); // set the hours
d.setMinutes(); // set the minutes
