// JS Get date Methods

const d = new Date();

d.getTime(); // get the time from 1970.
d.getFullYear(); // get the year.
d.getMonth() + 1; // get the month and we need to add 1 because month starts from 0.
d.getDate(); // get the returns
d.getHours() + 1; // get the hours and add 1 because hour also start from 0.
d.getMilliseconds(); // get the milliseconds
d.getDay(); // get the day and day starts from Sunday.
