// js functions

function sleep(names, time) {
  console.log(names, " is sleeping at ", time);
}

// names, time are the parameter declared inside the parenthesis.

// the function will not work until we call it like this
sleep("faysal", "10 PM");

// faysal, 10 PM are the arguments.

// inside the functions the arguments(the parameters) behave as local variables.

// the code inside the function will only occure when something invokes(call) the function.
