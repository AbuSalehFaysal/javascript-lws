// const keyword

// const variable cannot be reassigned.

// const variable value should be assigned while declaring the variable.

// always use const when you declare an array, object, function, regular expression.

// array, object are like a box, by storing array, objects inside a const variable, you cannot change the memory location reference(simply the box), but you can change change the value of the array, object(value of the box).

// const works in the block scope just like let.

// const variables can be hoisted as well but it does not initial a default value unlike var variables.
