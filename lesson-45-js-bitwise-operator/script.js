// JS Bitwise Operations

// operation between the binary systems are called bitwise operations

// binary conversion
(3).toString(2); // return 11

// 3 & 5 // 011 & 101 // 001

// << left Shift
// >> Right Shift
// >>> Right(Zero Filled) Shift

// decimal to Binary
// 0b indicates that the number is a binary number
(0b110000).toString(10); // 48
