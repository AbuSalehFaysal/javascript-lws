// JavaScript Output

// By ID
document.getElementById("demo").innerHTML = 5 + 6;

// By Class
document.getElementsByClassName("demo-class").innerHTML = 5 + 6;

// document.write - replace everything in the document after loading the entire page. We use it only for testing.
document.write(5 + 6);

// window.alert - It is already in the system, so we can avoid window if we want.
window.alert(5 + 6);

// console - Use mostly for debugging purpose
console.log("Hello World");

// window.print - Open the printing interface when we want to print something.
window.print();
